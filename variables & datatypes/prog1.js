//variables & datatypes in JS


// To declare variables in JS below keywords can be used
var newVariable = "This is a string"; // Its not a good practice to use var keyword to declare a variable in JS as variable can be treated as a global variable.

// Instead of var keyword use let keyword for variable declaration
let newVar2 = "This is a new variable";

// Declaring a constant variable
const const1 = 23;


